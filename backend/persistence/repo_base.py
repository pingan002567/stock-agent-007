def _json(data: Any) -> str:
    if hasattr(data, "json") or hasattr(data, "model_dump"):
        data = model_to_dict(data)
    return json.dumps(data, ensure_ascii=False)


def _loads(raw: str) -> Dict[str, Any]:
    return json.loads(raw)
